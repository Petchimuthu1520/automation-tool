package com.twozo.extent.reports.formatter.list;

/**
 * Represents types of HTML lists.
 * Provides options for ordered (OL) and unordered (UL) lists.
 *
 * @version 1.0
 * @Author Navin Jones
 */
public enum ListFormat {

    /**
     * Represents an ordered list.
     * Ordered lists typically display list items with numbering or ordering.
     */
    OL,

    /**
     * Represents an unordered list.
     * Unordered lists typically display list items with bullet points or other markers.
     */
    UL,
    ;
}