package com.twozo.extent.reports.formatter.codeblock;

/**
 * Represents different types of programming languages commonly used for structured data representation.
 * Provides symbolic constants for JSON and XML, which are prevalent languages for data interchange and serialization.
 * <p>
 * Each constant represents a specific language type, allowing for type-safe usage in code.
 * <p>
 * Note: Additional language types can be added to this enum as needed in the future.
 *
 * @version 1.0
 * @Author Navin Jones
 */
public enum CodeLanguage {

    /**
     * Represents the JSON (JavaScript Object Notation) language.
     */
    JSON,

    /**
     * Represents the XML (Extensible Markup Language) language.
     */
    XML,
}