package com.extent.reports.Markup;

/**
 * Enumeration representing types of HTML lists.
 * This enum provides options for ordered (OL) and unordered (UL) lists.
 */
public enum ListFormat {

    /**
     * Represents an ordered list (&lt;ol&gt; tag in HTML).
     * Ordered lists typically display list items with numbering or ordering.
     */
    OL,

    /**
     * Represents an unordered list (&lt;ul&gt; tag in HTML).
     * Unordered lists typically display list items with bullet points or other markers.
     */
    UL,
    ;

    // No additional methods or constants are defined here, as this enum only represents list types.
}

