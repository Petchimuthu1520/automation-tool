/**
 * Represents different types of web browsers.
 *
 * @author petchimuthu1520
 * @version 1.0
 */
enum BrowserType {

    /**
     * Google Chrome browser.
     */
    CHROME,

    /**
     * Mozilla Firefox browser.
     */
    FIREFOX,

    /**
     * Apple Safari browser.
     */
    SAFARI,

    /**
     * Internet Explorer browser.
     */
    INTERNET_EXPLORER,

    /**
     * Microsoft Edge browser.
     */
    EDGE;
}
